// ─── HDF5 BPCells probe ──────────────────────────────────────────────────────

const H5_BPCELLS_FIXTURE: &str = "../../tests/golden/seurat_v5_bpcells.h5seurat";

/// Probe an H5Seurat file for BPCells-backed assays.
///
/// Skips silently when the fixture is absent; run with `-- --nocapture` to see
/// output.  Place a Seurat v5 .h5seurat file at `tests/golden/seurat_v5_bpcells.h5seurat`
/// (generated e.g. via `SaveH5Seurat` after `as.BPCells`) to exercise this path.
#[test]
fn inspect_bpcells_h5seurat() {
    let path = Path::new(H5_BPCELLS_FIXTURE);
    if !path.exists() {
        println!("SKIP inspect_bpcells_h5seurat: fixture not found at {H5_BPCELLS_FIXTURE}");
        return;
    }

    let file = hdf5::File::open(path).expect("open h5seurat");

    // Collect candidate group paths to probe.  Seurat v5 typically writes
    // BPCells matrices under "assays/{assay}/layers/{layer}" or
    // "assays/{assay}/{layer}".
    let mut candidates: Vec<String> = Vec::new();
    if let Ok(assays_grp) = file.group("assays") {
        for assay in assays_grp.member_names().unwrap_or_default() {
            // v5 path: assays/{assay}/layers/{layer}
            let layers_path = format!("assays/{assay}/layers");
            if let Ok(layers_grp) = file.group(&layers_path) {
                for layer in layers_grp.member_names().unwrap_or_default() {
                    candidates.push(format!("assays/{assay}/layers/{layer}"));
                }
            }
            // v3/v4 path: assays/{assay}/{layer}
            if let Ok(assay_grp) = file.group(&format!("assays/{assay}")) {
                for member in assay_grp.member_names().unwrap_or_default() {
                    let p = format!("assays/{assay}/{member}");
                    candidates.push(p);
                }
            }
        }
    }

    println!("Probing {} candidate group(s):", candidates.len());
    let mut found_any = false;
    for path_str in &candidates {
        match probe_bpcells_version(&file, path_str) {
            Some(version) => {
                println!("  [BPCells v={version}] {path_str}");
                match open_bpcells_h5(&file, path_str, 1000) {
                    Ok(reader) => {
                        let (n_obs, n_vars) = reader.shape();
                        println!("    shape: n_obs={n_obs}, n_vars={n_vars}");
                    }
                    Err(e) => println!("    open_bpcells_h5 error: {e}"),
                }
                found_any = true;
            }
            None => println!("  [dgCMatrix / other] {path_str}"),
        }
    }

    if !found_any {
        println!("No BPCells groups detected in {H5_BPCELLS_FIXTURE}");
    }
}

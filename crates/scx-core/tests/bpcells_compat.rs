// ─── 4. DATASET READER TESTS ────────────────────────────────────────────────
// BpcellsDatasetReader implements the DatasetReader trait.

/// Helper: consume x_stream and reconstruct a dense row-major matrix.
async fn stream_to_dense(reader: &mut BpcellsDatasetReader) -> Vec<f64> {
    let (n_obs, n_vars) = reader.shape();
    let mut dense = vec![0.0f64; n_obs * n_vars];
    let mut stream = reader.x_stream();
    while let Some(chunk) = stream.next().await {
        let chunk = chunk.unwrap();
        let csr = &chunk.data;
        for row in 0..chunk.nrows {
            let obs = chunk.row_offset + row;
            let start = csr.indptr[row] as usize;
            let end = csr.indptr[row + 1] as usize;
            for k in start..end {
                let var = csr.indices[k] as usize;
                let v = match &csr.data {
                    scx_core::dtype::TypedVec::U32(vs) => vs[k] as f64,
                    scx_core::dtype::TypedVec::F32(vs) => vs[k] as f64,
                    scx_core::dtype::TypedVec::F64(vs) => vs[k],
                    scx_core::dtype::TypedVec::I32(vs) => vs[k] as f64,
                };
                dense[obs * n_vars + var] = v;
            }
        }
    }
    dense
}


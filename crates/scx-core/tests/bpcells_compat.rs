/// Print every file in synth_packed_uint_csc: name, size, 8-byte header.
/// Run with: cargo test -p scx-core inspect_bpcells -- --nocapture
#[test]
fn inspect_bpcells_packed_uint() {
    let dir = fixture("synth_packed_uint_csc");
    if !dir.exists() {
        eprintln!("SKIP: fixture not found — run `pixi run -e bpcells prepare-bpcells`");
        return;
    }
    let mut entries: Vec<_> = std::fs::read_dir(&dir)
        .unwrap()
        .map(|e| e.unwrap())
        .collect();
    entries.sort_by_key(|e| e.file_name());
    for e in &entries {
        let path = e.path();
        let size = path.metadata().unwrap().len();
        let data = std::fs::read(&path).unwrap();
        let hdr = String::from_utf8_lossy(&data[..data.len().min(8)]);
        println!(
            "  {:<28} {:>8} bytes  header={:?}",
            e.file_name().to_str().unwrap(),
            size,
            hdr
        );
    }
}


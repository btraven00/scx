/// BP-128-D1Z: 128-boundary — exactly one full chunk, sorted indices 0..127.
///
/// zigzag-encoded deltas: `[0, 2, 2, ..., 2]` (one zero then 127 twos).
/// B = 2 bits. Expected word pattern: lane 0 word 0 = 0xAAAAAAAA (alternating bits for val=2),
/// but lane 0 position 0 = 0, so word 0 = 0b10101010...0 = 0xAAAAAAAA - the lane-0 bit pattern
/// for [0, 2, 2, ...] is still 0xAAAAAAAA since position 0 in lane 0 is 0 (bit pattern 00)
/// and subsequent lane-0 positions are 2 (bit pattern 10).
/// Confirmed by Python reference decoder.
#[test]
fn codec_d1z_decode_128_boundary() {
    let packed: [u32; 8] = [
        2863311528, 2863311530, 2863311530, 2863311530, 2863311530, 2863311530, 2863311530,
        2863311530,
    ];
    let chunk_offsets = [0u32, 8];
    let starts = [0u32];
    let expected: Vec<u32> = (0..128).collect();

    let decoded = decode_d1z(&packed, &chunk_offsets, &starts, 128);
    assert_eq!(decoded, expected);
}

/// Zigzag encoding round-trip for a mix of positive and negative values.
#[test]
fn codec_zigzag_round_trip() {
    let cases: &[(i32, u32)] = &[
        (0, 0),
        (1, 2),
        (-1, 1),
        (2, 4),
        (-2, 3),
        (i32::MAX, u32::MAX - 1),
        (i32::MIN, u32::MAX),
    ];
    for &(raw, expected) in cases {
        let enc = zigzag_encode(raw);
        assert_eq!(enc, expected, "zigzag_encode({raw})");
        let dec = zigzag_decode(expected);
        assert_eq!(dec, raw, "zigzag_decode({expected})");
    }
}

// ─── 2b. PARALLEL CODEC TESTS ───────────────────────────────────────────────
// Verify that the Rayon parallel decode paths produce identical results to the
// hand-computed expected output, regardless of thread count.
//
// Both tests use a b=0 synthetic stream (zero packed words per chunk) to keep
// fixture generation trivial while exercising the full parallel dispatch.
// N_CHUNKS = 266 > 256 (RAYON_CHUNK_THRESHOLD) → always hits the parallel path.

/// Parallel decode_for produces correct results across thread counts.
///
/// b=0 stream: every chunk decodes to 128 ones (packed value 0 → v = 0+1 = 1).
#[test]
fn parallel_for_matches_sequential() {
    const N_CHUNKS: usize = 266;
    const COUNT: usize = N_CHUNKS * 128;

    let data: Vec<u32> = vec![];
    let offsets: Vec<u32> = vec![0u32; N_CHUNKS + 1];
    let expected = vec![1u32; COUNT];

    for n_threads in [1, 2, 4, rayon::current_num_threads()] {
        let result = rayon::ThreadPoolBuilder::new()
            .num_threads(n_threads)
            .build()
            .unwrap()
            .install(|| decode_for(&data, &offsets, COUNT));
        assert_eq!(
            result, expected,
            "decode_for wrong with {n_threads} thread(s)"
        );
    }
}

/// Parallel decode_d1z produces correct results across thread counts.
///
/// b=0 stream with starts[k]=k: all deltas are zero, so chunk k outputs 128
/// copies of k. Easy to verify without encoding anything.
#[test]
fn parallel_d1z_matches_sequential() {
    const N_CHUNKS: usize = 266;
    const COUNT: usize = N_CHUNKS * 128;

    let data: Vec<u32> = vec![];
    let offsets: Vec<u32> = vec![0u32; N_CHUNKS + 1];
    let starts: Vec<u32> = (0..N_CHUNKS as u32).collect();

    // chunk k → 128 copies of k
    let expected: Vec<u32> = (0..N_CHUNKS as u32)
        .flat_map(|k| std::iter::repeat(k).take(128))
        .collect();

    for n_threads in [1, 2, 4, rayon::current_num_threads()] {
        let result = rayon::ThreadPoolBuilder::new()
            .num_threads(n_threads)
            .build()
            .unwrap()
            .install(|| decode_d1z(&data, &offsets, &starts, COUNT));
        assert_eq!(
            result, expected,
            "decode_d1z wrong with {n_threads} thread(s)"
        );
    }
}

